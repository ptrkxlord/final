    inline BrowserConfig DetectBrowser() {
        char path[MAX_PATH];
        GetModuleFileNameA(NULL, path, MAX_PATH);
        std::wstring fullPath(Core::ToWide(path));
        std::wstring lowerPath = fullPath;
        std::transform(lowerPath.begin(), lowerPath.end(), lowerPath.begin(), ::towlower);

        auto configs = GetConfigs();

        if (lowerPath.find(L"msedge.exe") != std::wstring::npos) {
            if (lowerPath.find(L"\\edge beta\\") != std::wstring::npos) return configs.at("edge-beta");
            if (lowerPath.find(L"\\edge dev\\") != std::wstring::npos) return configs.at("edge-dev");
            if (lowerPath.find(L"\\edge canary\\") != std::wstring::npos) return configs.at("edge-canary");
            return configs.at("edge");
        }

        if (lowerPath.find(L"chrome.exe") != std::wstring::npos) {
            if (lowerPath.find(L"\\chrome sx s\\") != std::wstring::npos || lowerPath.find(L"\\chrome sxs\\") != std::wstring::npos) return configs.at("chrome-canary");
            if (lowerPath.find(L"\\chrome dev\\") != std::wstring::npos) return configs.at("chrome-dev");
            if (lowerPath.find(L"\\chrome beta\\") != std::wstring::npos) return configs.at("chrome-beta");
            if (lowerPath.find(L"\\chromium\\") != std::wstring::npos) return configs.at("chromium");
            return configs.at("chrome");
        }

        if (lowerPath.find(L"brave.exe") != std::wstring::npos) return configs.at("brave");
        if (lowerPath.find(L"avastbrowser.exe") != std::wstring::npos) return configs.at("avast");
        if (lowerPath.find(L"opera.exe") != std::wstring::npos) {
            if (lowerPath.find(L"\\opera gx stable\\") != std::wstring::npos) return configs.at("opera-gx");
            return configs.at("opera");
        }
        if (lowerPath.find(L"vivaldi.exe") != std::wstring::npos) return configs.at("vivaldi");
        if (lowerPath.find(L"browser.exe") != std::wstring::npos && lowerPath.find(L"\\yandexbrowser\\") != std::wstring::npos) return configs.at("yandex");
        if (lowerPath.find(L"coccoc.exe") != std::wstring::npos) return configs.at("coccoc");
        if (lowerPath.find(L"360se.exe") != std::wstring::npos) return configs.at("360se");
        if (lowerPath.find(L"360chrome.exe") != std::wstring::npos) return configs.at("360chrome");
        if (lowerPath.find(L"qqbrowser.exe") != std::wstring::npos) return configs.at("qq");
        if (lowerPath.find(L"sogouexplorer.exe") != std::wstring::npos) return configs.at("sogou");
        if (lowerPath.find(L"ucbrowser.exe") != std::wstring::npos) return configs.at("uc");
        if (lowerPath.find(L"maxthon.exe") != std::wstring::npos) return configs.at("maxthon");
        if (lowerPath.find(L"baidubrowser.exe") != std::wstring::npos) return configs.at("baidu");
        if (lowerPath.find(L"duckduckgo.exe") != std::wstring::npos) return configs.at("duckduckgo");
        if (lowerPath.find(L"iridium.exe") != std::wstring::npos) return configs.at("iridium");
        if (lowerPath.find(L"firefox.exe") != std::wstring::npos) return configs.at("firefox");
        if (lowerPath.find(L"waterfox.exe") != std::wstring::npos) return configs.at("waterfox");
        
        // CentBrowser often uses chrome.exe but in a different path
        if (lowerPath.find(L"chrome.exe") != std::wstring::npos && lowerPath.find(L"\\centbrowser\\") != std::wstring::npos) return configs.at("centbrowser");

        throw std::runtime_error("Unknown browser process");
    }

